CREATE TABLE IF NOT EXISTS financedwcontrols.kc2_control_results
(
    control_results_id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY
    ,inserttimestamp TIMESTAMP WITHOUT TIME ZONE NOT NULL DEFAULT (CURRENT_TIMESTAMP AT TIME ZONE 'US/Central')
    ,updatetimestamp TIMESTAMP WITHOUT TIME ZONE NOT NULL DEFAULT (CURRENT_TIMESTAMP AT TIME ZONE 'US/Central')
    ,load_datetime TIMESTAMP WITHOUT TIME ZONE NOT NULL DEFAULT (CURRENT_TIMESTAMP AT TIME ZONE 'US/Central')
    ,batch_id BIGINT NOT NULL
    ,cycle_date DATE NOT NULL
    ,source_system_name VARCHAR(50) NOT NULL
    ,"domain" VARCHAR(100) NOT NULL
    ,hop_name VARCHAR(255)
    ,src_control_log_id BIGINT
    ,tgt_control_log_id BIGINT
    ,control_id VARCHAR(100) NOT NULL
    ,fact_col_name VARCHAR(100)
    ,group_column_name VARCHAR(MAX)
    ,group_column_value VARCHAR(MAX)
    ,kc2kdc_indicator VARCHAR(25) NOT NULL
    ,operation VARCHAR(50) NOT NULL
    ,source_fact_value NUMERIC(18,2)
    ,target_fact_value NUMERIC(18,2)
    ,fact_value_variance NUMERIC(18,2)
    ,match_indicator CHAR(1)
    ,comments VARCHAR(255)
    ,UNIQUE (src_control_log_id, tgt_control_log_id)
    ,UNIQUE (batch_id, cycle_date, source_system_name, control_id, fact_column_name, group_column_name, group_column_value)
    ,FOREIGN KEY (domain) REFERENCES financedorchestration.hash_definition(domain_name)
    ,FOREIGN KEY (src_control_log_id) REFERENCES financedwcontrols.kc2_controllogging(control_log_id)
    ,FOREIGN KEY (tgt_control_log_id) REFERENCES financedwcontrols.kc2_controllogging(control_log_id)
);
